#█  Runner: Notice                 █
#█  • Уведомление (запуск команд)  █
#█                                 █
#▀─────────────────────────────────▀
runner:Notice() { case "$1" in
#┌───────────────────────────────────┐
#│ Проверяет пуст-ли текущий каталог │
#└───────────────────────────────────┘
    'current_dir_is_empty')
        controller:Request 'current_dir_is_empty'
    ;;
    
#┌───────────────────────────────────────┐
#│ Проверяет запущен-ли старый контейнер │
#└───────────────────────────────────────┘
    'run_old')
        [[ -n "$RUN_IMAGE" && "$RUN_VERSION" != "$VERSION" ]]
    ;;
    
#┌────────────────────────────────┐
#│ Проверяет запущен-ли контейнер │
#└────────────────────────────────┘
    'run')
        [ -n "$RUN_IMAGE" ]
    ;;
    
#┌─────────────────────────────────────────────────┐
#│ Проверяет существует-ли каталог с именем ".git" │
#└─────────────────────────────────────────────────┘
    'no_git')
        controller:Request 'no_git'
    ;;
    
#┌──────────────────────────────────────┐
#│ Проверяет задана-ли переменная '$CD' │
#└──────────────────────────────────────┘
    'no_cd')
        [ -z "$CD" ]
    ;;
esac
}
